/**
 * Regenerate lib/supabase/database.types.ts from the linked Supabase project.
 *
 * Prerequisites:
 *   - Supabase CLI installed (npm i -D supabase, or global install)
 *   - Logged in: supabase login
 *   - Project linked: supabase link --project-ref <ref>
 *
 * Local alternative:
 *   supabase start && npm run supabase:types:local
 */

import { execSync } from "node:child_process";
import { existsSync } from "node:fs";
import path from "node:path";

const root = process.cwd();
const outFile = path.join(root, "lib", "supabase", "database.types.ts");
const useLocal = process.argv.includes("--local");
const projectId = process.env.SUPABASE_PROJECT_ID;

const header = `/* eslint-disable */
/* Auto-generated by npm run supabase:types — do not edit manually */\n\n`;

function run(command) {
  console.log(`> ${command}`);
  execSync(command, { stdio: "inherit", cwd: root });
}

try {
  if (useLocal) {
    run(`npx supabase gen types typescript --local > "${outFile}"`);
  } else if (projectId) {
    run(`npx supabase gen types typescript --project-id ${projectId} > "${outFile}"`);
  } else if (existsSync(path.join(root, "supabase", ".temp"))) {
    run(`npx supabase gen types typescript --linked > "${outFile}"`);
  } else {
    console.error(
      "Link a project (supabase link) or pass --local, or set SUPABASE_PROJECT_ID."
    );
    process.exit(1);
  }

  console.log(`\nWrote ${outFile}`);
} catch (error) {
  console.error("Type generation failed:", error.message);
  process.exit(1);
}
